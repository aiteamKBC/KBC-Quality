# Database - Neon PostgreSQL
# Priority: DATABASE_URL_AITEAMKBC → DATABASE_URL (legacy fallback)
# DATABASE_URL_MAIN and DATABASE_URL_AUX are wired as additional named
# connections so views can reference them via using='main' / using='aux'.
_db_aiteamkbc = (
    os.environ.get('DATABASE_URL_AITEAMKBC') or
    os.environ.get('DATABASE_URL') or
    ''
)
_db_main = os.environ.get('DATABASE_URL_MAIN') or _db_aiteamkbc
_db_aux  = os.environ.get('DATABASE_URL_AUX')  or _db_aiteamkbc

def _make_db(url: str) -> dict:
    cfg = dj_database_url.config(default=url, conn_max_age=600)
    cfg.setdefault('OPTIONS', {})['sslmode'] = 'require'
    return cfg

DATABASES = {
    'default':   _make_db(_db_aiteamkbc),
    'aiteamkbc': _make_db(_db_aiteamkbc),
    'main':      _make_db(_db_main),
    'aux':       _make_db(_db_aux),
}

